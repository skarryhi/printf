#include "../header/ft_printf.h"

size_t		ft_intlen(int n)
{
	size_t c;

	c = 0;
	if (n == 0)
		return (1);
	if (n == -2147483648)
		return (11);
	if (n < 0)
	{
		n *= -1;
		c++;
	}
	while (n > 0)
	{
		n /= 10;
		c++;
	}
	return (c);
}
