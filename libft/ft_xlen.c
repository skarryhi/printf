#include "../header/ft_printf.h"

size_t		ft_xlen(unsigned int n)
{
	size_t c;

	c = 0;
	if (n == 0)
		return (1);
	while (n != 0)
	{
		n /= 16;
		c++;
	}
	return (c);
}
